#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	char	*s;
	size_t	i;

	s = (char *)malloc(sizeof(str));
	s = str;
	i = 0;
	while (i < n)
	{
		s[i] = '\0';
		i++;
	}
}
